class Mapa {
    worldMap: number[][];
    ctx: p5.p5InstanceExtensions
    constructor(ctx: p5.p5InstanceExtensions) {
        this.worldMap = []
        this.ctx = ctx
        this.getMap()
    }

    getMap() {
        for (let i = 0; i < mini_map.length; i++) {
            this.worldMap[i] = [];
            for (let j = 0; j < mini_map[0].length; j++) {
                this.worldMap[i][j] = 0;
            }
          }

        mini_map.forEach((row, i) => {
            row.forEach((col, j) => {
                if (col) {
                    this.worldMap[i][j] = col
                }
            })
        })
        console.log(this.worldMap)
                  
    }

    drawMap() {
         this.worldMap.forEach((row, i) => {
            row.forEach((col, j) => {
                if (col) {
                    this.ctx.fill(45)
                    this.ctx.rect(j * 100, i * 100, 100, 100)
                }
            })
        }) 
    }
}

